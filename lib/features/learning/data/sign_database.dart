  static List<SignModel> byCategory(SignCategory category) =>
      all.where((s) => s.category == category).toList();

  static Map<SignCategory, List<SignModel>> grouped() {
    final map = <SignCategory, List<SignModel>>{};
    for (final sign in all) {
      map.putIfAbsent(sign.category, () => []).add(sign);
    }
    return map;
  }

  static SignModel? findById(String id) =>
      all.where((s) => s.id == id).firstOrNull;
